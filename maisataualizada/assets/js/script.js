document.addEventListener('DOMContentLoaded', function () {
    // Identificar o elemento de conteúdo editável
    var nomempresaElement = document.getElementById('nomempresa');

    // Carregar conteúdo salvo
    var savedContent = localStorage.getItem('nomempresaContent');
    if (savedContent) {
        nomempresaElement.innerHTML = savedContent;
    }

    // Adicionar um ouvinte de evento para salvar o conteúdo quando for alterado
    nomempresaElement.addEventListener('input', function () {
        localStorage.setItem('nomempresaContent', nomempresaElement.innerHTML);
    });
});
   
